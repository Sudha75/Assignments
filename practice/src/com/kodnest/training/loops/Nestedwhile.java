package com.kodnest.training.loops;
import java.util.*;
public class Nestedwhile {
	public static void main(String[] args) {
        int num = 5;
        int i = 1;

        while (i <= 10) {
            int result = num * i;
            System.out.println(num + " * " + i + " = " + result);
            i++;
        }
    }
	

}


